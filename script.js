let counter = 0;

function upvote() {
 counter  = counter + 1;

 document.getElementById("votes").innerHTML = "Понравилось " +  counter + " диванным трейдерам";
}

function downvote() {
 counter  = counter - 1;

 document.getElementById("votes").innerHTML = "Понравилось " +  counter + " диванным трейдерам";
}

